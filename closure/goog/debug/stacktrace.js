/**
 * @fileoverview Stack trace generating utilities.
 *
 * @see ../demos/debug.html
 */

goog.provide('goog.debug.stackTrace');


/**
 * Max depth of stack to try to output.
 * @type {number}
 * @const
 * @private
 */
goog.debug.stackTrace.MAX_STACK_DEPTH_ = 50;


/**
 * Hash map for storing function names that have already been looked up.
 * @type {!Object}
 * @private
 */
goog.debug.stackTrace.fnNameCache_ = {};


/**
 * Resolves functions to their names.  Resolved function names will be cached.
 * @type {function(Function):string}
 * @private
 */
goog.debug.stackTrace.fnNameResolver_;


/**
 * Gets the current stack trace. Simple and iterative - doesn't worry about
 * catching circular references or getting the args.
 * @param {number=} opt_depth Optional maximum depth to trace back to.
 * @return {string} A string with the function names of all functions in the
 *     stack, separated by \n.
 */
goog.debug.stackTrace.getStacktraceSimple = function(opt_depth) {
  var sb = [];
  var fn = arguments.callee.caller;
  var depth = 0;

  while (fn && (!opt_depth || depth < opt_depth)) {
    sb.push(goog.debug.stackTrace.getFunctionName(fn));
    sb.push('()\n');
    /** @preserveTry */
    try {
      fn = fn.caller;
    } catch (e) {
      sb.push('[exception trying to get caller]\n');
      break;
    }
    depth++;
    if (depth >= goog.debug.stackTrace.MAX_STACK_DEPTH_) {
      sb.push('[...long stack...]');
      break;
    }
  }
  if (opt_depth && depth >= opt_depth) {
    sb.push('[...reached max depth limit...]');
  } else {
    sb.push('[end]');
  }

  return sb.join('');
};


/**
 * Gets the current stack trace, either starting from the caller or starting
 * from a specified function that's currently on the call stack.
 * @param {Function=} opt_fn Optional function to start getting the trace from.
 *     If not provided, defaults to the function that called this.
 * @return {string} Stack trace.
 */
goog.debug.stackTrace.getStacktrace = function(opt_fn) {
  return goog.debug.stackTrace.getStacktraceHelper_(
      opt_fn || arguments.callee.caller, []);
};


/**
 * Private helper for getStacktrace().
 * @param {Function} fn Function to start getting the trace from.
 * @param {Array} visited List of functions visited so far.
 * @return {string} Stack trace starting from function fn.
 * @private
 */
goog.debug.stackTrace.getStacktraceHelper_ = function(fn, visited) {
  var sb = [];

  // Circular reference, certain functions like bind seem to cause a recursive
  // loop so we need to catch circular references
  var circularReference = false;
  for (var i = 0; i < visited.length; i++) {
    if (visited[i] == fn) {
      circularReference = true;
      break;
    }
  }

  if (circularReference) {
    sb.push('[...circular reference...]');

  // Traverse the call stack until function not found or max depth is reached
  } else if (fn && visited.length < goog.debug.stackTrace.MAX_STACK_DEPTH_) {
    sb.push(goog.debug.stackTrace.getFunctionName(fn) + '(');
    var args = fn.arguments;
    for (var i = 0; i < args.length; i++) {
      if (i > 0) {
        sb.push(', ');
      }
      var argDesc;
      var arg = args[i];
      switch (typeof arg) {
        case 'object':
          argDesc = arg ? 'object' : 'null';
          break;

        case 'string':
          argDesc = arg;
          break;

        case 'number':
          argDesc = String(arg);
          break;

        case 'boolean':
          argDesc = arg ? 'true' : 'false';
          break;

        case 'function':
          argDesc = goog.debug.stackTrace.getFunctionName(arg);
          argDesc = argDesc ? argDesc : '[fn]';
          break;

        case 'undefined':
        default:
          argDesc = typeof arg;
          break;
      }

      if (argDesc.length > 40) {
        argDesc = argDesc.substr(0, 40) + '...';
      }
      sb.push(argDesc);
    }
    visited.push(fn);
    sb.push(')\n');
    /** @preserveTry */
    try {
      sb.push(goog.debug.stackTrace.getStacktraceHelper_(fn.caller, visited));
    } catch (e) {
      sb.push('[exception trying to get caller]\n');
    }

  } else if (fn) {
    sb.push('[...long stack...]');
  } else {
    sb.push('[end]');
  }
  return sb.join('');
};


/**
 * Set a custom function name resolver.
 * @param {function(Function): string} resolver Resolves functions to their
 *     names.
 */
goog.debug.stackTrace.setFunctionResolver = function(resolver) {
  goog.debug.stackTrace.fnNameResolver_ = resolver;
};


/**
 * Gets a function name
 * @param {Function} fn Function to get name of.
 * @return {string} Function's name.
 */
goog.debug.stackTrace.getFunctionName = function(fn) {
  if (goog.debug.stackTrace.fnNameCache_[fn]) {
    return goog.debug.stackTrace.fnNameCache_[fn];
  }
  if (goog.debug.stackTrace.fnNameResolver_) {
    var name = goog.debug.stackTrace.fnNameResolver_(fn);
    if (name) {
      // TODO(user): Use goog.getUid(fn) as key instead of fn since the
      // current behaviour is implementation dependent.
      goog.debug.stackTrace.fnNameCache_[fn] = name;
      return name;
    }
  }

  // Heuristically determine function name based on code.
  var functionSource = String(fn);
  if (!goog.debug.stackTrace.fnNameCache_[functionSource]) {
    var matches = /function ([^\(]+)/.exec(functionSource);
    if (matches) {
      var method = matches[1];
      goog.debug.stackTrace.fnNameCache_[functionSource] = method;
    } else {
      goog.debug.stackTrace.fnNameCache_[functionSource] = '[Anonymous]';
    }
  }

  return goog.debug.stackTrace.fnNameCache_[functionSource];
};
